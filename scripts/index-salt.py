# --- Module index generation (LLM-oriented YAML) ---

_SECRET_RE = re.compile(r"(?:tg_secret|gopass_secret)\s*\(\s*'([^']+)'")
_SERVICE_FILE_RE = re.compile(r"user_service_file\s*\(\s*'[^']+'\s*,\s*'([^']+)'")
_SERVICE_ENABLE_RE = re.compile(
    r"user_service_enable\s*\(\s*'[^']+'(?:\s*,\s*start_now\s*=\s*\[([^\]]*)\])?"
)
_CONTAINER_RE = re.compile(r"container_service\s*\(\s*'([^']+)'")
_CONFIG_RE = re.compile(r"salt://(configs/[^\s'\"]+)")
_DATA_IMPORT_RE = re.compile(r"import_yaml\s+['\"](data/[^'\"]+)['\"]")
_NPM_PKG_RE = re.compile(r"npm_pkg\s*\(\s*'[^']+'\s*,\s*pkg\s*=\s*'([^']+)'")
_DOC_COMMENT_RE = re.compile(r"\{#\s*(.*?)\s*#\}", re.DOTALL)


def _scan_state_source(relpath):
    """Scan a .sls file for secrets, services, configs, data refs."""
    full = os.path.join(STATES_DIR, relpath.removeprefix("states/"))
    secrets = []
    services = []
    configs = []
    data_files = []
    purpose = ""
    try:
        with open(full) as f:
            src = f.read()
    except (OSError, IOError):
        return secrets, services, configs, data_files, purpose

    secrets = sorted(set(_SECRET_RE.findall(src)))
    # Collapse spaces inside Jinja {{ }} to avoid regex truncation on spaces
    _jinja_safe = re.sub(r"\{\{\s*(.*?)\s*\}\}", r"{{\1}}", src)
    configs = sorted(set(_CONFIG_RE.findall(_jinja_safe)))
    data_files = sorted(set(o for o in _DATA_IMPORT_RE.findall(src) if o.startswith("data/")))

    # Also scan referenced config .j2 templates for data imports
    for config_rel in configs:
        config_path = os.path.join(STATES_DIR, config_rel)
        if not config_path.endswith(".j2"):
            continue
        if not os.path.isfile(config_path):
            continue
        try:
            with open(config_path) as cf:
                config_src = cf.read()
        except (OSError, IOError):
            continue
        for j2_match in _DATA_IMPORT_RE.finditer(config_src):
            j2_rel = j2_match.group(1)
            if j2_rel.startswith("data/"):
                data_files.append(j2_rel)

    data_files = sorted(set(data_files))

    for m in _SERVICE_FILE_RE.finditer(src):
        services.append(m.group(1))
    for m in _CONTAINER_RE.finditer(src):
        services.append(f"{m.group(1)}.container")
    for m in _NPM_PKG_RE.finditer(src):
        services.append(f"{m.group(1)} (npm)")

    # First line after header or doc comment as purpose hint
    doc_m = _DOC_COMMENT_RE.search(src)
    if doc_m:
        purpose = doc_m.group(1).strip().split("\n")[0].strip()

    return secrets, sorted(set(services)), configs, data_files, purpose


def _find_tests_for_state(state_name):
    """Heuristic: find test files that likely test a given state."""
    base = os.path.basename(state_name).removeprefix("test_").removesuffix(".sls").replace(".", "_")
    matches = []
    for f in sorted(glob.glob("tests/test_*.py")):
        name = os.path.basename(f).removeprefix("test_").removesuffix(".py")
        if name == base or name == base.replace("_", ""):
            matches.append(f)
    return matches


def _find_tests_for_data(data_basename):
    base = data_basename.removesuffix(".yaml")
    match = f"tests/test_{base}.py"
    return [match] if os.path.isfile(match) else []


def generate_module_index(state_results, macros, summaries, usage):
    """Generate docs/module-index.yaml — LLM-oriented project map."""

    index = {
        "version": 1,
        "generated": datetime.now().strftime("%Y-%m-%dT%H:%M:%S"),
    }

    # States
    states_list = []
    for rel, state_ids, includes, requires, guards in state_results:
        state_name = _state_name_from_relpath(rel)
        secrets, services, configs, data_files, purpose = _scan_state_source(rel)
        tests = _find_tests_for_state(rel)

        entry = {
            "id": state_name,
            "path": f"states/{rel}" if not rel.startswith("states/") else rel,
            "purpose": purpose,
            "feature_gate": sorted(set(guards)) if guards else None,
            "secrets": secrets if secrets else None,
            "includes": sorted(set(includes)) if includes else None,
            "data_files": data_files if data_files else None,
            "services": services if services else None,
            "configs": configs if configs else None,
            "tests": tests if tests else None,
        }
        # Remove None fields for compactness
        entry = {k: v for k, v in entry.items() if v is not None}
        states_list.append(entry)
    index["states"] = states_list

    # Macros
    macros_list = []
    by_file = {}
    for name, params, src, doc in macros:
        by_file.setdefault(src, []).append(name)
    for src in sorted(by_file):
        macros_list.append({
            "id": os.path.basename(src).replace(".jinja", ""),
            "path": src,
            "provides": by_file[src],
        })
    index["macros"] = macros_list

    # Python scripts
    py_scripts = []
    for f in sorted(glob.glob("scripts/*.py")):
        py_scripts.append({
            "path": f,
            "purpose": _purpose_from_docstring(f),
        })
    index["scripts_py"] = py_scripts

    # Shell scripts
    sh_scripts = []
    for f in sorted(glob.glob("scripts/*.sh") + glob.glob("scripts/*.zsh")):
        sh_scripts.append({
            "path": f,
            "purpose": _purpose_from_shebang_comment(f),
        })
    index["scripts_sh"] = sh_scripts

    # Data files
    data_list = []
    for rel, _ in summaries:
        data_key = os.path.join(STATES_DIR, rel.removeprefix("states/"))
        consumers = usage.get(data_key, [])
        data_list.append({
            "path": rel,
            "consumers": sorted(consumers) if consumers else None,
        })
    index["data_files"] = data_list

    # Test files
    test_list = []
    for f in sorted(glob.glob("tests/test_*.py")):
        test_list.append({
            "path": f,
        })
    index["test_files"] = test_list

    # Docs
    docs_list = []
    for f in sorted(glob.glob("docs/*.md")):
        docs_list.append({
            "path": f,
            "purpose": _purpose_from_first_heading(f),
        })
    index["docs"] = docs_list

    return yaml.dump(index, default_flow_style=False, allow_unicode=True, sort_keys=False)


def validate_knowledge_references(knowledge_path, state_results, macros, summaries):
    """Cross-validate knowledge.yaml related_entities/related_docs against project entities.

    Returns list of (severity, message) tuples.
    """
    with open(knowledge_path) as f:
        knowledge = yaml.safe_load(f)

    # Build entity lookup sets
    state_ids = set()
    for rel, _state_ids, _includes, _requires, _guards in state_results:
        state_ids.add(_state_name_from_relpath(rel))

    macro_ids = set()
    for _name, _params, src, _doc in macros:
        macro_ids.add(os.path.basename(src).replace(".jinja", ""))

    script_basenames = set()
    for f in glob.glob("scripts/*.py"):
        script_basenames.add(os.path.basename(f))
    for f in glob.glob("scripts/*.sh") + glob.glob("scripts/*.zsh"):
        script_basenames.add(os.path.basename(f))

    data_basenames = set()
    for rel, _info in summaries:
        data_basenames.add(os.path.basename(rel))
        data_basenames.add(rel)

    service_names = set()
    for rel, _state_ids, _includes, _requires, _guards in state_results:
        _s, services, _c, _d, _p = _scan_state_source(rel)
        for svc in services:
            service_names.add(svc)
            service_names.add(svc.split(".")[0].split(" (")[0])

    all_doc_ids = set()
    for cat in knowledge.get("categories", {}).values():
        for doc_id in cat.get("docs", {}):
            all_doc_ids.add(doc_id)

    issues = []

    for cat_name, category in knowledge.get("categories", {}).items():
        for doc_id, doc in category.get("docs", {}).items():
            for ref in doc.get("related_entities", []):
                if ref in ("state:*", "macro:*"):
                    continue
                if ref == "macro:_macros_*.jinja":
                    if not any(fnmatch.fnmatch(mid, "_macros_*") for mid in macro_ids):
                        issues.append(("error", f"[{cat_name}/{doc_id}] {ref}: no matching macros"))
                    continue

                prefix, name = ref.split(":", 1)

                if prefix == "state":
                    if name not in state_ids:
                        alt = name.replace("-", "_").replace(".", "_")
                        if alt not in state_ids:
                            issues.append((
                                "warn",
                                f"[{cat_name}/{doc_id}] {ref}: state not found in module index",
                            ))
                elif prefix == "macro":
                    mid = name.replace(".jinja", "")
                    if mid not in macro_ids:
                        issues.append(("error", f"[{cat_name}/{doc_id}] {ref}: macro not found"))
                elif prefix == "data":
                    if name not in data_basenames:
                        issues.append((
                            "error",
                            f"[{cat_name}/{doc_id}] {ref}: data file not found",
                        ))
                elif prefix == "script":
                    if name not in script_basenames:
                        issues.append(("error", f"[{cat_name}/{doc_id}] {ref}: script not found"))
                elif prefix == "service":
                    if name not in service_names:
                        issues.append((
                            "warn",
                            f"[{cat_name}/{doc_id}] {ref}: "
                            "service not found in any state definition",
                        ))

            for ref in doc.get("related_docs", []):
                if ref not in all_doc_ids:
                    issues.append((
                        "error",
                        f"[{cat_name}/{doc_id}] related_docs '{ref}': "
                        "target doc not found in knowledge.yaml",
                    ))

    return issues


def _purpose_from_docstring(path):
    try:
        with open(path) as f:
            src = f.read()
        m = re.search(r'"""(.+?)"""', src, re.DOTALL)
        if m:
            return m.group(1).strip().split("\n")[0].strip()
    except Exception:
        pass
    return ""


def _purpose_from_shebang_comment(path):
    try:
        with open(path) as f:
            lines = f.readlines()
        for line in lines[:5]:
            line = line.strip()
            if line.startswith("# ") and not line.startswith("#!"):
                return line[2:].strip()
    except Exception:
        pass
    return ""


def _purpose_from_first_heading(path):
    try:
        with open(path) as f:
            for line in f:
                if line.startswith("# "):
                    return line[2:].strip()
    except Exception:
        pass
    return ""


def _log(pretty, msg: str) -> None:
    if pretty:
        pretty.info(msg)
    else:
        print(msg)


def main():
    if not os.path.isdir(STATES_DIR):
        print(f"Error: {STATES_DIR} not found. Run from project root.", file=sys.stderr)
        sys.exit(1)

    os.makedirs(MEMORY_DIR, exist_ok=True)
    os.makedirs(DOCS_DIR, exist_ok=True)

    try:
        from lib.pretty import pretty
    except ImportError:
        pretty = None

    if pretty:
        pretty.section("Indexing Salt States")

    # 1. Macros
    jinja_files = sorted(glob.glob(os.path.join(STATES_DIR, "_macros_*.jinja")))
    macros = parse_macros(jinja_files)
    macros_md = generate_macros_md(macros)
    macros_path = os.path.join(MEMORY_DIR, "salt-macros.md")
    with open(macros_path, "w") as f:
        f.write(macros_md)
    _log(pretty, f"Wrote {macros_path} ({len(macros)} macros, {macros_md.count(chr(10))} lines)")

    # 2. States
    sls_files = [record.relpath for record in discover_state_files(STATES_DIR)]
    state_results = render_states(sls_files)
    rendered_ok = sum(1 for _, ids, _, _, _ in state_results if ids)
    states_md = generate_states_md(state_results)
    states_path = os.path.join(MEMORY_DIR, "salt-states.md")
    with open(states_path, "w") as f:
        f.write(states_md)
    n_lines = states_md.count(chr(10))
    _log(
        pretty,
        f"Wrote {states_path} ({len(sls_files)} files, {rendered_ok} rendered, {n_lines} lines)",
    )

    # 3. Data files
    summaries = summarize_data_files()
    data_md = generate_data_md(summaries)
    data_path = os.path.join(MEMORY_DIR, "salt-data.md")
    with open(data_path, "w") as f:
        f.write(data_md)
    _log(pretty, f"Wrote {data_path} ({len(summaries)} files, {data_md.count(chr(10))} lines)")

    usage = collect_data_usage()
    macro_usage = collect_macro_data_usage()
    for k, v in macro_usage.items():
        usage.setdefault(k, set()).update(v)
    usage = {k: sorted(v) for k, v in usage.items()}
    eng_inventory, _ = generate_data_inventory_docs(summaries, usage)
    inventory_path = os.path.join(DOCS_DIR, "data-inventory.md")
    with open(inventory_path, "w") as f:
        f.write(eng_inventory)
    _log(pretty, f"Wrote {inventory_path}")

    # 4. State dependency map docs
    graph, reverse, guards_map = build_state_graph(state_results)
    eng_map, _ = generate_state_map_docs(graph, reverse, guards_map)
    eng_path = os.path.join(DOCS_DIR, "state-map.md")
    with open(eng_path, "w") as f:
        f.write(eng_map)
    _log(pretty, f"Wrote {eng_path}")

    # 5. Module index (LLM-oriented YAML)
    module_index_yaml = generate_module_index(state_results, macros, summaries, usage)
    module_index_path = os.path.join(DOCS_DIR, "module-index.yaml")
    with open(module_index_path, "w") as f:
        f.write(module_index_yaml)
    _log(pretty, f"Wrote {module_index_path}")

    write_knowledge_base(macros, state_results, summaries)

    # 6. Cross-validate knowledge.yaml references
    knowledge_path = os.path.join(DOCS_DIR, "knowledge.yaml")
    if os.path.isfile(knowledge_path):
        issues = validate_knowledge_references(knowledge_path, state_results, macros, summaries)
        if issues:
            errs = sum(1 for sev, _ in issues if sev == "error")
            warns = sum(1 for sev, _ in issues if sev == "warn")
            msg = f"knowledge.yaml validation: {errs} errors, {warns} warnings"
            if pretty:
                if errs:
                    pretty.fail(msg)
                else:
                    pretty.warn(msg)
                for severity, msg_line in issues:
                    if severity == "error":
                        pretty.fail(f"  {msg_line}")
                    else:
                        pretty.warn(f"  {msg_line}")
            else:
                print(f"\n{msg}")
                for severity, msg_line in issues:
                    print(f"  [{severity.upper()}] {msg_line}")
        else:
            if pretty:
                pretty.ok("knowledge.yaml validation: all references OK")
            else:
                print("knowledge.yaml validation: all references OK")

if __name__ == "__main__":
    main()
