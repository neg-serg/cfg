# --- Macro parsing ---

MACRO_DEF_RE = re.compile(r"\{%-?\s*macro\s+(\w+)\s*\((.*?)\)\s*-?%\}", re.DOTALL)


def _state_name_from_relpath(relpath):
    rel = relpath.replace("\\", "/")
    if rel.startswith("states/"):
        rel = rel[len("states/") :]
    return rel.removesuffix(".sls").replace("/", ".")


def parse_macros(jinja_files):
    """Parse macro definitions from .jinja files.

    Returns list of (name, params_str, source_file, doc_comment).
    """
    macros = []
    for path in sorted(jinja_files):
        with open(path) as f:
            source = f.read()

        rel = os.path.relpath(path)
        # Find doc comments immediately before macros
        for m in MACRO_DEF_RE.finditer(source):
            name = m.group(1)
            params = m.group(2).strip()
            # Look for {# ... #} comment in the 500 chars before macro
            pre = source[max(0, m.start() - 500) : m.start()]
            doc = ""
            doc_m = re.search(r"\{#\s*(.*?)\s*#\}", pre, re.DOTALL)
            if doc_m:
                doc = doc_m.group(1).strip().split("\n")[0].strip()
            macros.append((name, params, rel, doc))
    return macros


def generate_macros_md(macros):
    """Generate salt-macros.md content."""
    lines = [HEADER, "# Salt Macro Index\n"]

    by_file = {}
    for name, params, src, doc in macros:
        by_file.setdefault(src, []).append((name, params, doc))

    for src in sorted(by_file):
        lines.append(f"\n## `{src}`\n")
        for name, params, doc in by_file[src]:
            lines.append(f"### {name}")
            lines.append(f"```\n{name}({params})\n```")
            if doc:
                lines.append(f"_{doc}_")
            lines.append("")

    return "\n".join(lines) + "\n"


